# Operadores de asignacion
# Igual = 
# Incremento +=
# Decremento -=
# Multiplicacion *=
# Division /=
# Potencia **=
numero = 100
numero -= 5
print(numero)

# Operadores de comparacion
# == para saber si son el mismo valor
# != para saber si el son diferentes
# <, > para saber si es menor que o mayor que
# <=, >= para saber si es menor o igual que o mayor o igual que

print(5 >= 5)

# Operadores Logicos
# and = si los condicionales son verdaderos entonces todo es verdadero
# or = si uno de los condicionales es verdadero todo es verdadero
# not = si el condicional es falso entonces se volvera verdadero
# EN JS el and es && 
# EN JS el or es ||
# EN JS el not es ! 
print(not((10<20) and (50>80)))

